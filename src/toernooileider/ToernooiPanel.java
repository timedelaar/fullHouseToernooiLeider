    private void getToernooien () {
        String query = "SELECT  Evenement.evenementID, Evenement.datum, Evenement.locatieID, Evenement.naam, ToernooiSoort.beschrijving "
                + "FROM Evenement "
                + "INNER JOIN  Toernooi ON Evenement.evenementID = Toernooi.evenementID "
                + "JOIN ToernooiSoort ON Toernooi.soortToernooi = ToernooiSoort.soortID "
                + "ORDER BY Evenement.datum ASC;";
        try {
            Connection conn = SimpleDataSource.getConnection();
            PreparedStatement stat = conn.prepareStatement(query);
            ResultSet result = stat.executeQuery();
            fillTable(result);
            result.close();
            stat.close();
        }
        catch (Exception e) {
            //FullHouse.showDBError(e);
            System.out.println(e);
        }
    }
        
        
        private void fillTable(ResultSet result) throws SQLException {
            String[] columnNames = {"ID", "Evenement naam", "Soort", "Locatie", "Datum"};
            DefaultTableModel model = new DefaultTableModel();
            model.setDataVector(new Object[][]{}, columnNames);
            while (result.next()) {
            String ID = result.getString("evenementID");
            Date datum = result.getDate("datum");
            int locatie = result.getInt("locatieID");
            String naam = result.getString("naam");
            String soort = result.getString("beschrijving");
            Object[] rowData = {ID, naam, soort, locatie, datum};
            model.addRow(rowData);
            }
        toernooiTable.setModel(model);
        result.close();
        
    }
    
        
  
    
