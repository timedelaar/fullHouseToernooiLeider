    private void selecteerButMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_selecteerButMouseClicked
        int[] row = toernooiTable.getSelectedRows();
        if (row.length == 0)
        {
            JOptionPane.showMessageDialog(this, "Geen toernooi geselecteerd!", "Fout", JOptionPane.PLAIN_MESSAGE);
        }
        else if (row.length == 1)
        {
            int ID;
            ID = Integer.parseInt(toernooiTable.getValueAt(row[0], 0).toString());
            toernooi.setEvenementID(ID);
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Selecteer 1 toernooi!", "Fout", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_selecteerButMouseClicked

